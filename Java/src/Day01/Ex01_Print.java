package Day01;

public class Ex01_Print {
	// 프로그램 실행 : ctrl + F11
	
	// main : ctrl + space 
	// 프로그램이 시작되는 메소드 
	public static void main(String[] args) {
	
		// 기본 출력 메소드 
		// - print(), println() 메소드를 호출하여 출력한다. 
		// - System.out 출력 객체를 사용한다. 
		// sysout : ctrl + space 
		// main 메소드 안에서만 사용가능 
		System.out.print("안녕하세요");
		
		
		// println();       - ln (line)
		// : 지정한 문자열을 출력한 후, 한 줄 엔터
		System.out.println("안녕하세요 출력후 줄바꿈");
		System.out.println("자바 첫 수업 ");
		System.out.println("이어지는 문자열 ");
		
		// shift + enter : 어디에 있든 바로 다음줄로 이동 		
		
	}
	
}
